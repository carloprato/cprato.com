<p><strong>Group members WILL / Membri tal-grupp se:<br /><br /></strong></p>
<ul>
<li>Respect the role of the group facilitator</li>
<li>Nirrispettaw ir-rwol tal-faċilitatur tal- grupp</li>
</ul>
<p></p>
<ul>
<li>Respect for each other</li>
<li>Nirrespettaw lil xulxin</li>
</ul>
<p></p>
<ul>
<li>Be courteous to others at all times</li>
<li>Inkunu dejjem kordiali lejn l-ohrajn</li>
</ul>
<p></p>
<ul>
<li>LISTEN to each other</li>
<li>Nisimghu lil xulxin</li>
</ul>
<p></p>
<ul>
<li>Allow others to speak and have their say</li>
<li>Naghtu cans lil hadd iehor jitkellem</li>
</ul>
<p></p>
<ul>
<li>Take time out if needed</li>
<li>Tista tiehu break jekk tahseb li ghandek bzonn</li>
</ul>
<p></p>
<ul>
<li>Keep to time</li>
<li>Irrispetta il hin tieghek meta ikun immisek titkellem</li>
</ul>
<p></p>
<ul>
<li>Abide by the group rules on confidentiality and unacceptable behaviour</li>
<li>Nimxu mar-regoli tal-grupp dwar il-kunfidenzjalit&agrave; u mġiba mhux aċċettabbli, nafdaw lil xulxin u dak li jintqal hawn jibqa hawn</li>
</ul>
<p></p>
<ul>
<li>Refrain from using offensive language</li>
<li>Noqogħdu lura milli nużaw lingwaġġ offensiv</li>
</ul>
<p></p>
<ul>
<li>Refrain from using sexist or racist comments or behaviour</li>
<li>Inzommu lura milli nużaw kummenti jew imġieba dwar religion, politika, rassizmu u intimita personali</li>
<li>Nirspettaw li spazju personali ta l- individwu</li>
</ul>
<p></p>
<p><strong><br />Group members WILL NOT / Mhux permess</strong></p>
<ul>
<li>Drown out the voices of others or monopolise discussions</li>
<li>Mhux permess li waqt persuna tkun qed titkellem naqbsu fil konversazjoni u nikontrollawha.</li>
</ul>
<p></p>
<ul>
<li>Impose their views on others</li>
<li>M ghandnhiex nimponu l-opinjoni personali taghna fuq haddiehor</li>
</ul>
<p></p>
<ul>
<li>Consume alcohol during meetings</li>
<li>Mhux permess alkahol</li>
</ul>
<p></p>
<ul>
<li>Bring young children to the group</li>
<li>Tfal mhux permessi li jattendu</li>
</ul>
<p></p>
<ul>
<li>Exploit other group members sexually, physically, emotionally, financially or in any other way</li>
<li>Nisfruttaw membri oħra tal-grupp sesswalment, fiżikament, emozzjonalment, finanzjarjament jew b'xi mod ieħor</li>
</ul>
<p></p>
<ul>
<li><strong>Anyone who infringes the rules may be asked to leave the forum / meeting</strong></li>
<li>Kull min jikser ir-regoli jistgħu jiġu mitluba li jħallu l-laqgħa.</li>
</ul>{endlayout:evidence}{endlayout}